/// semaphore create syscall
pub fn sys_semaphore_create(res_count: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_create",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let id = if let Some(id) = process_inner
        .semaphore_list
        .iter()
        .enumerate()
        .find(|(_, item)| item.is_none())
        .map(|(id, _)| id)
    {
        process_inner.semaphore_list[id] = Some(Arc::new(Semaphore::new(res_count)));
        process_inner.semaphore_avail[id] = res_count;
        // 遍历alloc 和 need 把他们列为id的项赋值为0
        for i in 0..process_inner.semaphore_alloc.len() {
            process_inner.semaphore_alloc[i][id] = 0;
            process_inner.semaphore_need[i][id] = 0;
        }
        id
    } else {
        process_inner
            .semaphore_list
            .push(Some(Arc::new(Semaphore::new(res_count))));
        process_inner.semaphore_avail.push(res_count);
        for i in 0..process_inner.semaphore_alloc.len() {
            process_inner.semaphore_alloc[i].push(0);
            process_inner.semaphore_need[i].push(0);
        }
        process_inner.semaphore_list.len() - 1
    };
    id as isize
}
/// semaphore up syscall
pub fn sys_semaphore_up(sem_id: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_up",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());

    process_inner.semaphore_avail[sem_id] += 1;  // 增加可用资源
    drop(process_inner);  // 释放锁，避免影响 `sem.up`
    sem.up();  // 唤醒等待中的线程
    0
}
/// semaphore down syscall
pub fn sys_semaphore_down(sem_id: usize) -> isize {
    trace!(
        "kernel:pid[{}] tid[{}] sys_semaphore_down",
        current_task().unwrap().process.upgrade().unwrap().getpid(),
        current_task()
            .unwrap()
            .inner_exclusive_access()
            .res
            .as_ref()
            .unwrap()
            .tid
    );
    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();
    let sem = Arc::clone(process_inner.semaphore_list[sem_id].as_ref().unwrap());
    let tid = current_task()
        .unwrap()
        .inner_exclusive_access()
        .res
        .as_ref()
        .unwrap()
        .tid;

    // 增加资源需求
    process_inner.semaphore_need[tid][sem_id] += 1;

    // 死锁检测
    if process_inner.deadlock_detect() == -1 {
        process_inner.semaphore_need[tid][sem_id] -= 1;  // 回滚需求
        return -0xDEAD;
    }

    drop(process_inner);  // 释放锁，避免影响 `sem.down`

    sem.down();  // 获取信号量

    let process = current_process();
    let mut process_inner = process.inner_exclusive_access();

    // 更新资源状态
    process_inner.semaphore_need[tid][sem_id] -= 1;
    process_inner.semaphore_avail[sem_id] -= 1;
    process_inner.semaphore_alloc[tid][sem_id] += 1;

    0
}
